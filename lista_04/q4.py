"""Lista 04 - Condicional Simples

Q4. Faça um algoritmo que lê dois números, e verifica se o primeiro número é igual ao segundo número. Se forem iguais, escrever "Números iguais". Se não, escrever "Números diferentes".

"""

numero1 = int(input("Digite um número: "));

numero2 = int(input("Digite outro número: "));

if numero1 == numero2:
    print("Números iguais.");

else:
    print("Números diferentes.");