"""
LISTA 02 - Variáveis e Operações Matemáticas

Q8. Área de um Retângulo: Escreva um algoritmo que solicita ao usuário a largura e o comprimento de um retângulo, depois calcula e exibe a área do retângulo. A fórmula para calcular a área de um retângulo é área = largura x comprimento.

"""

largura_retangulo = int(input("Digite a largura do retângulo: "));

comprimento_retangulo = int(input("Digite o comprimento do retângulo: "));

area_retangulo = largura_retangulo * comprimento_retangulo;

print("A área do retângulo de largura", largura_retangulo, "e comprimento", comprimento_retangulo,"é:", area_retangulo);