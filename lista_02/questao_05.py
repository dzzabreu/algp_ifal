"""
LISTA 02 - Variáveis e Operações Matemáticas

Q5. Escreva um algoritmo que leia a nota de 3 estudantes e retorne a média das notas.

"""

nota_estudante01 = float(input("Qual a nota do estudante 01? "));

nota_estudante02 = float(input("Qual a nota do estudante 02? "));

nota_estudante03 = float(input("Qual a nota do estudante 03? "));

media_notas = (nota_estudante01 + nota_estudante02 + nota_estudante03) / 3;

print("A média das notas dos 3 estudantes é: ", media_notas);